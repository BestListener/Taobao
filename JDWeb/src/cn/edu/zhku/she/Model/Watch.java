package cn.edu.zhku.she.Model;

public class Watch extends Product{
	private String brank;
	private String watchNo;
	private String watchApp;
	private String watchMove;
	private String watchStyle;
	private String watchProduce;
	private String watchDisplay;
	private String watchStrap;
	public Watch()
	{
		super();
	}
	public String getBrank() {
		return brank;
	}
	public void setBrank(String brank) {
		this.brank = brank;
	}
	public String getWatchNo() {
		return watchNo;
	}
	public void setWatchNo(String watchNo) {
		this.watchNo = watchNo;
	}
	public String getWatchApp() {
		return watchApp;
	}
	public void setWatchApp(String watchApp) {
		this.watchApp = watchApp;
	}
	public String getWatchMove() {
		return watchMove;
	}
	public void setWatchMove(String watchMove) {
		this.watchMove = watchMove;
	}
	public String getWatchStyle() {
		return watchStyle;
	}
	public void setWatchStyle(String watchStyle) {
		this.watchStyle = watchStyle;
	}
	public String getWatchProduce() {
		return watchProduce;
	}
	public void setWatchProduce(String watchProduce) {
		this.watchProduce = watchProduce;
	}
	public String getWatchDisplay() {
		return watchDisplay;
	}
	public void setWatchDisplay(String watchDisplay) {
		this.watchDisplay = watchDisplay;
	}
	public String getWatchStrap() {
		return watchStrap;
	}
	public void setWatchStrap(String watchStrap) {
		this.watchStrap = watchStrap;
	}
}
